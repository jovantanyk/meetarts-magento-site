namespace Webkul\MpBuyerSellerChat\Model\ResourceModel\CustomerData;

use \Webkul\MpBuyerSellerChat\Model\ResourceModel\AbstractCollection;

/**
 * Webkul MpBuyerSellerChat ResourceModel Seller collection
 */
class Collection extends AbstractCollection
{
    /**
     * @var string
     */
    protected $_idFieldName = 'entity_id';

    /**
     * Define resource model
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_init(
            \Webkul\MpBuyerSellerChat\Model\CustomerData::class,
            \Webkul\MpBuyerSellerChat\Model\ResourceModel\CustomerData::class
        );
    }
    
    /**
     * Add filter by store
     *
     * @param int|array|\Magento\Store\Model\Store $store
     * @param bool $withAdmin
     * @return $this
     */
    public function addStoreFilter($store, $withAdmin = true)
    {
        if (!$this->getFlag('store_filter_added')) {
            $this->performAddStoreFilter($store, $withAdmin);
        }
        return $this;
    }
}
